sub new {
    my $classname = shift;
    my $self = {};
    bless($self, $classname);
    $self->_init(@_);
    return $self;
}

sub DESTROY {
    my $self = shift;
}

sub disconnect {
    my $self = shift;
    $self->{socket}->close();
}

sub _init {
    my $self = shift;
    $self->{host} = "";
    $self->{port} = 0;
    $self->{user} = "";
    $self->{password} = "";
    $self->{socket} = undef;

    $self->{authenticated} = 0;
    $self->{recv_seq} = 0;
    $self->{send_seq} = 0;
    $self->{timestamp} = time();

    $self->{error} = 0;

    $self->{client} = undef;

    $self->{log} = "syslog";


    if (@_) {
        my %extra = @_;
        @$self{keys %extra} = values %extra;
    }

}


sub FormatBinaryArray {
    my $self = shift;

    my (@binary_data) = @_;

    my @formatted_data = map {sprintf("%02x", $_)} @binary_data;

    my $hex_data = join(' ', @formatted_data);
    return $hex_data;
}

sub FormatBinary {
    my $self = shift;

    my ($binary) = @_;
    my @binary_data = unpack('C*', $binary);

    my @formatted_data = map {sprintf("%02x", $_)} @binary_data;

    my $hex_data = join(' ', @formatted_data);
    return $hex_data;
}


sub ProcessBinaryPacket {
    my $self = shift;

    my ($pkt_head, $pkt_data) = @_;

    my $hex_data = $self->FormatBinary($pkt_data);

    $self->LogPacketData($pkt_head, $pkt_data);
    $self->LogPacketRawData($pkt_head, $pkt_data);

    return $hex_data;

}

sub LogFilenamePrefix {
    my $self = shift;

    my $pkt_head = $_[0];

    my $fileprefix = sprintf("nvserver-pkt-%s-0x%04x", $self->{client}, $pkt_head->{type});

    return $fileprefix;

}


sub LogPacketData {
    my $self = shift;
    my ($pkt_head, $pkt_data) = @_;

    my $hex_data = $self->FormatBinary($pkt_data);
    my $filename = $self->LogFilenamePrefix($pkt_head) . ".dat";

    open(OUT, ">> $filename");
    print OUT $self->FormatTimestamp(time()) . " " . $self->get_logprefix() . $hex_data . "\n";
    close(OUT);
}


sub LogExtraPacketData {
    my $self = shift;
    my ($pkt_head, $pkt_data, $postfix) = @_;

    my $hex_data = $self->FormatBinary($pkt_data);
    my $filename = $self->LogFilenamePrefix($pkt_head) . ".dat";

    open(OUT, ">> $filename");
    print OUT $self->FormatTimestamp(time()) . " " . $self->get_logprefix() . $hex_data . " $postfix\n";
    close(OUT);
}

sub LogPacketRawData {
    my $self = shift;
    my ($pkt_head, $pkt_data) = @_;

    my $filename = $self->LogFilenamePrefix($pkt_head) . ".bin";

    open(OUT, ">> $filename");
    print OUT $pkt_head->{raw} . $pkt_data;
    close(OUT);
}


sub FormatTimestamp {
    my $self = shift;

    my $timestamp = $_[0];
    my ($sec, $min, $hour, $mday, $mon, $year, $wday, $yday, $isdst) = localtime($timestamp);

    return sprintf("%02d.%02d.%4d %02d:%02d:%02d", $mday, $mon + 1, $year + 1900, $hour, $min, $sec);
}

sub ArraySplitBlocks {
    my $self = shift;

    my @arr = @{$_[0]};
    my $blocksize = $_[1];
    my @final = ();
    my $i = 0;

    while ($i <= $#arr) {

        my $j = $i;
        my @tmp = ();
        while ($j < ($i + $blocksize) && $j <= $#arr) {
            #print sprintf("%d ", $arr[$j]);
            push(@tmp, $arr[$j]);
            #print "j=$j ";
            $j++;
        }
        push(@final, \@tmp);

        print "\n";
        $i += $blocksize;
    }

    return \@final;

}


