sub GetPacketHead {

    my $self = shift;
    my $data;
    my @reply_head_array;
    my $ignore_errors = $_[0];
    my $rawhead = '';
    my $badpacket = 0;
    my $reply_head = {
        protocol       => 0,
        size_sum       => 0,
        Content_Length => 0,
        sequence       => 0,
        type           => 0,
        size           => 0,
        unknown_s      => 0,
        error          => 0,
        raw            => '',
        head_2         => 0,
        head_3         => 0,
    };


    # protocol, version, reserved
    $self->{socket}->recv($data, 4, MSG_WAITALL);

    $rawhead .= $data;

    $self->{socket}->recv($data, 4, MSG_WAITALL);

    $rawhead .= $data;

    my $datalen = unpack('N', $data);

    my $size = $datalen;

    $self->write_log(sprintf("Datalen %d", $datalen));

    my @header = unpack('nn', $rawhead);

    my ($protocol, $type) = (@header)[0, 1];

    $reply_head_array[3] = ();

    $reply_head = {
        protocol       => $protocol,
        size_sum       => 0,
        Content_Length => $size,
        sequence       => 0,
        type           => $type,
        size           => $size,
        unknown_s      => 0,
        error          => 0,
        raw            => $rawhead,
        head_2         => 0,
        head_3         => $head_3,
    };

    my $seq_diff = $sequence - $self->{recv_seq};

    my $first_seq = 0;

    if ($self->{recv_seq} eq 0) {
        $first_seq = 1;
    }

    $self->{recv_seq} = $sequence;

    $self->write_log(sprintf("received head: protocol = 0x%02X seq = %d type = 0x%04X size = %d", $protocol, $sequence, $type, $size));

    if ($protocol ne PROTO_NETVIWER) {
        $self->write_log("Not a NetViewer packet!", "err");
        $badpacket = 1;
        $reply_head->{error} = 1;
        return $reply_head;
    }

    return $reply_head;
}


sub GetPacketData {
    my $self = shift;

    my $reply = $_[0];

    my $length = $reply->{size};

    my $out;
    if ($length > 0) {
        for (my $downloaded = 0; $downloaded < $length; $downloaded++) {
            $self->{socket}->recv($data, 1, MSG_WAITALL);
            $out .= $data;
        }
    }

    return $out;
}

sub get_logprefix {
    my $self = shift;
    #syslog('info', $_[0]);

    my $timestamp = $self->FormatTimestamp(time());
    my $host = "";
    if (defined($self->{socket})) {
        $host = $self->{socket}->peerhost();
    }

    $host = sprintf("%15s", $host);

    if ($self->{log} ne "syslog") {
        return "$timestamp $host nvserver[] ";
    }

    return "$host ";
}

sub write_log() {
    my $self = shift;

    my ($message, $facility) = @_;

    if ($facility ne 'err') {
        $facility = 'info';
    }

    if ($self->{log} ne "syslog") {
        my $filename = $self->{log};
        open(OUT, ">> $filename");
        print OUT $self->get_logprefix() . $message . "\n";
        close(OUT);
    }
    else {
        syslog($facility, $self->get_logprefix() . $message);
    }

}

