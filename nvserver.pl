sub ReadPacket {
    my $self = shift;
    my $reply_head;

    if ($self->{terminate} eq 1) {
        return 0;
    }

    $reply_head = $self->GetPacketHead();

    $self->{recv_counter} += 1;

    if ($reply_head->{error} eq 1) {
        $self->{terminate} = 1;
        return 0;
    }

    $reply_data = $self->GetPacketData($reply_head);

    my $received_size = length($reply_data);

    if ($received_size < $reply_head->{size}) {
        $self->write_log(sprintf("Received only %d data bytes from %d: %s", $received_size, $reply_head->{size}, $self->FormatBinary($reply_data)));
        $self->{terminate} = 1;
        return 0;
    }

    if ($reply_head->{type} eq TYPE_LOGIN_REQUEST || $reply_head->{type} eq 0x2ff) {
        $self->ProcessLoginPacket($reply_head, $reply_data);
        return 1;
    } elsif ($reply_head->{type} eq TYPE_PING) {
        $self->ProcessPingPacket($reply_head, $reply_data);
        return 1;
    } elsif ($reply_head->{type} eq TYPE_GET_SETTINGS_REQ) {
        $self->ProcessGetSettingsPacket($reply_head, $reply_data);
        return 1;
    } elsif ($reply_head->{type} eq TYPE_PTZ) {
        $self->ProcessPTZPacket($reply_head, $reply_data);
        return 1;
    } elsif ($reply_head->{type} eq 0x6101) {
        $self->Process6101Packet($reply_head, $reply_data);
        return 1;
    }

    elsif ($reply_head->{type} eq TYPE_END_SESSION) {

        $self->{terminate} = 1;
        return 0;
    }
    else {
        $self->ProcessDefaultPacket($reply_head, $reply_data);
        return 1;
    }

    return 0;
}


sub BuildPacket {
    my $self = shift;
    my ($type, $pkt_body, $direction) = @_;

    my $protocol = PROTO_NETVIWER;

    my $size = length($pkt_body) + 8;

    $self->{send_seq} += 1;

    my $pkt_head = pack('nnN', $protocol, $type, $size);

    my $unknown_s = 0;

    my $pkt_data = $pkt_head . $pkt_body;
    $self->write_log(sprintf("send: sequence = %d type = 0x%02X size = %d\n", $self->{send_seq}, $type, $size));

    return $pkt_data;

}


